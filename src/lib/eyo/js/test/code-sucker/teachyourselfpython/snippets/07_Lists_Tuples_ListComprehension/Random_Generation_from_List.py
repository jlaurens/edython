import random

#Generate a Random Number from 0 to 99
my_number = random.randrange(100)
print(my_number)

#Generate a random item from a predefined list
secret_colour = ["blue", "red", "yellow"]
random_index = random.randrange(3)
print(secret_colour[random_index])

#Generate a random floating point number from 0 to 1
my_number = random.random()
print(my_number)


                    
